#include <stdio.h>
int main()
{
// How data types works in c.
    int age = 18;
    float pi = 3.14;
    char asterisk = '*';
    // Format Specifier
    printf("Age is %d \n", age);
    // For Integer
    printf("Value of pi is %f \n", pi);
    // For real numbers
    printf("Asterisk looks like this %c \n", asterisk);
    // For Chareecters
    return 0;
}